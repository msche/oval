package net.sf.oval.internal.util;

/**
 * Type of collection (if any)
 */
public enum CollectionType {

    /**
     * Single value
     */
    SINGLE,

    /**
     * Collection of objects
     */
    COLLECTION,

    /**
     * Map of objects
     */
    MAP,

    /**
     * Array of objects
     */
    ARRAY;
}
